int32 pcm_ioctl(uint32 chan, uint32 cmd, uint32* val)
{
	uint32 reg;
	if(chan >= CH_NUM)
		return FAILED;
	switch(cmd){
		case PCM_GET_MODE:
			reg = REG32(PCSCR);
			if(!(reg & (CH0CMPE >> (chan * 8))))
				*(uint32*)val =  PCM_MODE_UNIFORM;
			else if(reg & (CH0Alaw >> (chan * 8)))
				*(uint32*)val = PCM_MODE_ALAW;
			else
				*(uint32*)val = PCM_MODE_ULAW;
			return SUCCESS;
		case PCM_GET_LOOPBACK:
			reg = REG32(PCSCR);
			if((chan == 0) && (reg & CH0ILBE))
				*(uint32*)val = TRUE;
			else
				*(uint32*)val = FALSE;
			return SUCCESS;
		case PCM_GET_TIMESLOT:
			*(uint32*)val = REG32(PTSAR);
			return SUCCESS;
		case PCM_SET_MODE:
			switch(val){
				case PCM_MODE_UNIFORM:
					REG32(PCSCR) &= ~(CH0CMPE >> (chan * 8));
					break;
				case PCM_MODE_ULAW:
					reg = REG32(PCSCR);
					reg |= (CH0CMPE >> (chan * 8));
					reg &= ~(CH0Alaw >> (chan * 8));
					REG32(PCSCR) = reg;
					break;
				case PCM_MODE_ALAW:
					reg = REG32(PCSCR);
					reg |= (CH0CMPE >> (chan * 8));
					reg |= (CH0Alaw >> (chan * 8));
					REG32(PCSCR) = reg;
					break;
				default:
					return FAILED;
				}
			return SUCCESS;
		case PCM_SET_LOOPBACK:
			if(chan == 0){
				if(val == TRUE)
					REG32(PCSCR) |= CH0ILBE;
				else
					REG32(PCSCR) &= ~CH0ILBE;
				return SUCCESS;
				}
			else
				return FALSE;
		case PCM_SET_TIMESLOT:
			if(val > 0x1f)
				return FAILED;
			reg = REG32(PTSAR);
			reg &= ~(0xff << ((CH_NUM - 1 - chan) * 8));
			reg |= (val << ((CH_NUM - 1 - chan) * 8));
			REG32(PTSAR) = reg;
			return SUCCESS;
		default:
			return FAILED;
		}
	return FAILED;
}

