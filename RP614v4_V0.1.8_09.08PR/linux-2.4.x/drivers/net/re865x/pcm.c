void memcpy_bin(void* out, void *in, int nBytes)
{
  	int i;
  	for( i = 0 ; i < nBytes ; i++ )
  	{
  		if(*((short int*)in + i ) > 0x70){
			*((char*)out+i) = 0x70;
  		}
		else if(*((short int*)in + i ) < 0xFF) {
			*((char*)out+i) = 0xFF;
		}
		else{
	  		*((char*)out+i) = *((short int*)in + i );
		}
  	}
}
void memcpy_bin2(void* out, void *in, int nBytes)
{
  	int i;
  	for( i = 0 ; i < nBytes ; i++ )
		*((char*)out+i) = *((short int*)in + i) >> 8;
	if( nBytes < PAGESIZE ){
		for( ; i < PAGESIZE ; i++ )
			*((char*)out+i) = 0;
	}
}

