int32 pcm_randTest(uint32 seed, uint32 count)
{
#define MAX_BYTES_IN_PAGE	(8000 * 2 * 3/100)	//8kHz * 2bytes * 30ms
#define MAXERR 1024
#define ERRAVE 512
#define RETRY 10000
#define CHK_INTR(ip, chan, str) \
		retry = 0; \
		while(1){ \
			if((REG32(PISR) & (ip >> (chan * 8))) == (ip >> (chan * 8))) \
				break; \
			 if(++retry > RETRY){ \
			 	printk(str); \
			 	ret = FAILED; \
				goto _exit; \
			 	} \
			 for(j=0; j<1000; j++); \
			}
	int		chan, mode, frame;
	uint8 	*buf;
	uint8	*txbsa[CH_NUM];
	uint8	*rxbsa[CH_NUM];
	uint32	pgsize;
	int		retry;
	int 		i, j;
	int32	round;
	uint32	txshift, rxshift;
	int32	ret = SUCCESS;
	
	/* config GPIO as PCM */
	REG32(0x200c) |= 0x00ff0000UL;
	REG32(0x2054) &= ~0x00ff0000UL;

	/* stop watchdog */
	REG32(0x203c) = 0xa5000000;

	/* allocate buffer */
	buf = (uint8*) rtlglue_malloc(MAX_BYTES_IN_PAGE * 2 * CH_NUM * 2);
	for(i=0; i<CH_NUM; i++){
		buf += (MAX_BYTES_IN_PAGE * 2/*pages*/);
		txbsa[i] = (uint8*) ((uint32) buf | 0x20000000UL);
		REG32(CH0TXBSA + (i * 4)) = (uint32) buf & 0x1fffffffUL;
		}
	for(i=0; i<CH_NUM; i++){
		buf += (MAX_BYTES_IN_PAGE * 2/*pages*/);
		rxbsa[i] = (uint8*) ((uint32) buf | 0x20000000UL);
		REG32(CH0RXBSA + (i * 4)) = (uint32) buf & 0x1fffffffUL;
		}
		
	printk("PCM random loopback test start ...\n");
	printk("random seed = %d\n", seed);
	tauset(0, seed);
	round = 0;
	for(;;){
		/* reset pcm every time to get rid of in-FIFO rx data */
		REG32(PGCR) = PCMENABLE|PCMCLKDIR;	// 0->1 enable PCM
		REG32(PGCR) = 0;
		REG32(PGCR) = PCMENABLE|PCMCLKDIR;	// 0->1 enable PCM
		
		/* random variables */
		chan = taurand(CH_NUM);
		printk("Channel %d ", chan);
		mode = taurand(3);
		printk("%s ", (mode == PCM_MODE_UNIFORM)? "Linear mode": 
									(mode == PCM_MODE_ULAW)? "u-law": "A-law");
		frame = taurand(2);
		printk("%s ", (frame == 0)? "20ms": "30ms");

		/* calculate page size */
		if(!frame)
			pgsize = 8000 * 2/100;
		else
			pgsize = 8000 * 3/100;
		if(mode != PCM_MODE_UNIFORM)
			pgsize *= 2;
//		printk("pgsize=%d ", pgsize);

		/* random data */
		for(i=0; i<(pgsize * 2/*pages*/); i++)
			txbsa[chan][i] = taurand(256);

		/* configure registers */
		REG32(PBSIZE) = ((pgsize >> 2) -1) << ((CH_NUM - 1 - chan) * 8);
		REG32(CH0TXBSA + (chan * 4)) |= PAGE0_OWN | PAGE1_OWN;
		REG32(CH0RXBSA + (chan * 4)) |= PAGE0_OWN | PAGE1_OWN;
		REG32(PIMR) = 0xf000 >> (chan * 4);
		REG32(PCSCR) = CH0ILBE|
			((CH0TE|CH0RE|((mode == PCM_MODE_UNIFORM)? 0: (mode == PCM_MODE_ULAW)? (CH0CMPE): (CH0CMPE|CH0Alaw))) >> (chan * 8));
		/* wait for tx page 0 ok interrupt */
		CHK_INTR(0x80000000UL, chan, "ERROR: Tx page 0 OK intr miss\n");
		/* wait for rx page 0 ok interrupt */
		CHK_INTR(0x20000000UL, chan, "ERROR: Rx page 0 OK intr miss\n");
		/* wait for tx page 1 ok interrupt */
		CHK_INTR(0x40000000UL, chan, "ERROR: Tx page 1 OK intr miss\n");
		/* wait for rx page 1 ok interrupt */
		CHK_INTR(0x10000000UL, chan, "ERROR: Rx page 1 OK intr miss\n");
		/* wait for tx page 0 unavailable interrupt */
		CHK_INTR(0x08000000UL, chan, "ERROR: Rx page 1 unavailable intr miss\n");
		/* wait for rx page 0 unavailable interrupt */
		CHK_INTR(0x02000000UL, chan, "ERROR: Rx page 1 unavailable intr miss\n");
		/* check own bits */
		retry = 0;
		while(1){
			 if((REG32(CH0RXBSA+chan*4) & (PAGE0_OWN | PAGE1_OWN)) == 0)
				break;
			 if(++retry > RETRY){
			 	printk("ERROR: Rx not complete\n");
			 	ret = FAILED;
				goto _exit;
			 	}
			 for(j=0; j<1000; j++);
			}
		
		/* compare data */
//		memDump(txbsa[chan], 64, "tx page 0");
//		memDump(txbsa[chan] + pgsize, 64, "tx page 1");
//		memDump(rxbsa[chan], 64, "rx page 0");
//		memDump(rxbsa[chan] + pgsize, 64, "rx page 1");
		if(mode == PCM_MODE_UNIFORM){
			/* rx data might be one-byte ahead or behind of tx data */
			rxshift = txshift = 0;
			if(txbsa[chan][0] != rxbsa[chan][0]){
				if(txbsa[chan][0] == rxbsa[chan][1]){
					rxshift = 1;
					printk("(1st byte inserted:%d) ", rxbsa[chan][0]);
					}
				else if(txbsa[chan][1] == rxbsa[chan][0]){
					txshift = 1;
					printk("(1st byte lost:%d) ", txbsa[chan][0]);
					}
				}
			if(memcmp(txbsa[chan] + txshift, rxbsa[chan] + rxshift, (pgsize * 2) - (txshift + rxshift))){
				printk("ERROR: Data not match\n");
				for(j=0; j<(pgsize * 2) - (txshift + rxshift); j++)
					if(txbsa[chan][j + txshift] != rxbsa[chan][j + rxshift])
						printk("byte error! tx %02x(%x) != rx %02x(%x)\n", 
								txbsa[chan][j + txshift], (uint32) txbsa[chan] + j + txshift, rxbsa[chan][j + rxshift], (uint32) rxbsa[chan] + j + rxshift);
				ret = FAILED;
				goto _exit;
				}
			}
		else{
#define TWORDOF(chan, index) (*((int16*) txbsa[chan] + index))
#define RWORDOF(chan, index) (*((int16*) rxbsa[chan] + index))
#define ERR5WORDS(chan, tidx,ridx) \
	(abs(TWORDOF(chan,tidx)-RWORDOF(chan,ridx)) + \
	abs(TWORDOF(chan,tidx+1)-RWORDOF(chan,ridx+1)) + \
	abs(TWORDOF(chan,tidx+2)-RWORDOF(chan,ridx+2)) + \
	abs(TWORDOF(chan,tidx+3)-RWORDOF(chan,ridx+3)) + \
	abs(TWORDOF(chan,tidx+4)-RWORDOF(chan,ridx+4)) \
	)
			int16 tx, rx;
			uint32 error;
			uint32 maxErr = 0;
			uint32 errAve = 0;
			uint32 err5words;
			/* rx data might be one-word ahead or behind of tx data */
			rxshift = txshift = 0;
			err5words = ERR5WORDS(chan,0,0);
			if(ERR5WORDS(chan,0,1) < err5words){
				rxshift = 1;
				printk("(1st word inserted:%d)", RWORDOF(chan,0));
				}
			else if(ERR5WORDS(chan,1,0) < err5words){
				txshift = 1;
				printk("(1st word lost:%d)", TWORDOF(chan,0));
				}
			for(j=0; j<pgsize - (txshift + rxshift); j++){
				tx = TWORDOF(chan, j + txshift);
				rx = RWORDOF(chan, j + rxshift);
				error = abs(tx - rx);
				if(error > maxErr)
					maxErr = error;
				errAve += error;
				}
			errAve /= pgsize;
			printk("Max error=%d, average error=%d ", maxErr, errAve);
			if((maxErr > MAXERR) || (errAve > ERRAVE)){
				printk("ERROR: Data not match\n");
				for(j=0; j<pgsize - (txshift + rxshift); j++){
					tx = TWORDOF(chan, j + txshift);
					rx = RWORDOF(chan, j + rxshift);
					error = abs(tx - rx);
					if(error > MAXERR)
						printk("word error! tx %04x(%x) != rx %04x(%x)\n", 
								tx, (uint32) ((uint16*) txbsa[chan] + j + txshift), rx, (uint32) ((uint16*) rxbsa[chan] + j + rxshift));
					}
				ret = FAILED;
				goto _exit;
				}
			}
		
		REG32(CH0TXBSA + (chan * 4)) |= PAGE0_OWN;
		REG32(CH0RXBSA + (chan * 4)) |= PAGE0_OWN;
		REG32(PISR) = REG32(PISR);
		/* wait for tx page 0 ok interrupt */
		CHK_INTR(0x80000000UL, chan, "ERROR: Tx page 0 OK intr miss\n");
		/* wait for rx page 0 ok interrupt */
		CHK_INTR(0x20000000UL, chan, "ERROR: Rx page 0 OK intr miss\n");
		/* wait for tx page 1 unavailable interrupt */
		CHK_INTR(0x04000000UL, chan, "ERROR: Rx page 1 unavailable intr miss\n");
		/* wait for rx page 1 unavailable interrupt */
		CHK_INTR(0x01000000UL, chan, "ERROR: Rx page 1 unavailable intr miss\n");
		
		REG32(CH0TXBSA + (chan * 4)) |= PAGE1_OWN;
		REG32(CH0RXBSA + (chan * 4)) |= PAGE1_OWN;
		REG32(PISR) = REG32(PISR);
		/* wait for tx page 1 ok interrupt */
		CHK_INTR(0x40000000UL, chan, "ERROR: Tx page 0 OK intr miss\n");
		/* wait for rx page 1 ok interrupt */
		CHK_INTR(0x10000000UL, chan, "ERROR: Rx page 0 OK intr miss\n");
		/* wait for tx page 0 unavailable interrupt */
		CHK_INTR(0x08000000UL, chan, "ERROR: Rx page 1 unavailable intr miss\n");
		/* wait for rx page 0 unavailable interrupt */
		CHK_INTR(0x02000000UL, chan, "ERROR: Rx page 1 unavailable intr miss\n");
		
		REG32(PCSCR) = 0;
		printk("PASS\n");

		if(++round >= count)
			break;
	}

_exit:
	rtlglue_free(buf);
	printk("%d rounds\n", round);
	return ret;
}

/*mode: 0 for u-law, 1 for a-law, 2 for linear test*/
int32 pcm_compenderTest(int32 mode)
{
/*480 bytes in 240 samples*/
#define BYTES_IN_PAGE	(8000 * 2 * 3/100)	//8kHz * 2bytes * 30ms
#define RETRY 10000
	uint8 	*buf;
	int16	*txbsa;
	int8	*rxbsa;
	int		retry;
	int 		i, j;
	int32	ret = SUCCESS;
	
	/* config GPIO as PCM */
	REG32(0x200c) |= 0x00ff0000UL;
	REG32(0x2054) &= ~0x00ff0000UL;

	/* stop watchdog */
	REG32(0x203c) = 0xa5000000;

	/* allocate buffer */
	buf   = (uint8*) rtlglue_malloc(BYTES_IN_PAGE * 2 * 2);
	txbsa = (uint16*) ((uint32) buf | 0x20000000UL);
	REG32(CH0TXBSA) =  (uint32) buf & 0x1fffffffUL;
	
	buf  += (BYTES_IN_PAGE * 2/*pages*/);
	
	rxbsa =  (uint8*) ((uint32) buf | 0x20000000UL);
	REG32(CH1RXBSA) =  (uint32) buf & 0x1fffffffUL;
		
	printk("PCM encompender test start ...\n");
	
	/* reset pcm every time to get rid of in-FIFO rx data */
	REG32(PGCR) = PCMENABLE|PCMCLKDIR;	// 0->1 enable PCM
	REG32(PGCR) = 0;
	REG32(PGCR) = PCMENABLE|PCMCLKDIR;	// 0->1 enable PCM
	
	/* prepare data */
	for(i=0; i<BYTES_IN_PAGE/2; i++)
		txbsa[i] = i;
	txbsa[0] = -2460;
	txbsa[1] = -1505;
	txbsa[2] = -650;
	txbsa[3] = -338;
	txbsa[4] = -90;
	txbsa[5] = -1;
	if(mode == 0) {
		txbsa[6] = 102;
		txbsa[7] = 169;
		txbsa[8] = 420;
		txbsa[9] = 499;
		txbsa[10] = 980;
		txbsa[11] = 7000;
	}
	else if(mode == 1){
		txbsa[6] = 40;
		txbsa[7] = 102;
		txbsa[8] = 169;
		txbsa[9] = 420;
		txbsa[10] = 499;
		txbsa[11] = 980;
	}

	/* configure registers */
	REG32(PTSAR) = 0x00010203;
	REG32(PBSIZE) = (((BYTES_IN_PAGE >> 2) -1)  << 24) | ((BYTES_IN_PAGE >> 2) << 16);
	REG32(CH0TXBSA) |= PAGE0_OWN;
	REG32(CH0RXBSA + 4) |= PAGE0_OWN | PAGE1_OWN;
	switch(mode){
		case 2:
			REG32(PCSCR) = CH0TE|CH1RE;
			printk("linear mode\n");
			break;
		case 1: //a-law
			REG32(PCSCR) = CH0TE|CH1RE|CH0CMPE|CH0Alaw;
			printk("a-law mode\n");
			break;
		case 0: //u-law
		default:
			REG32(PCSCR) = CH0TE|CH1RE|CH0CMPE;
			printk("u-law mode\n");
			break;
		}
	/* end of configure registers */
	retry = 0;
	while(1){
		 if((REG32(CH1RXBSA) & (PAGE0_OWN | PAGE1_OWN)) == 0)
			break;
		 if(++retry > RETRY){
		 	printk("ERROR: Rx not complete\n");
		 	ret = FAILED;
			goto _exit;
		 	}
		 for(j=0; j<1000; j++);
	}
		
	/* compare data */
	memDump(txbsa, BYTES_IN_PAGE, "tx page 0");
	memDump(rxbsa, BYTES_IN_PAGE, "rx page 0");
//	memDump(rxbsa + BYTES_IN_PAGE, BYTES_IN_PAGE, "rx page 1");
	
	/* compute result */
	if(mode == 0){
		for(i=0; i<BYTES_IN_PAGE/2; i++)
			rxbsa[i] = linear2ulaw(txbsa[i]);
		memDump(rxbsa, BYTES_IN_PAGE, "software computation");
		}
	else if (mode == 1){
		for(i=0; i<BYTES_IN_PAGE/2; i++)
			rxbsa[i] = linear2alaw(txbsa[i]);
		memDump(rxbsa, BYTES_IN_PAGE, "software computation");
		}
	
 	printk("Fail! Check if partner setup correctly\n");
	ret = FAILED;

_exit:
	rtlglue_free(buf);
	return ret;
}

void pcm_compenderTestPartner(void)
{
	/* config GPIO as PCM */
	REG32(0x200c) |= 0x00ff0000UL;
	REG32(0x2054) &= ~0x00ff0000UL;
	
	/* reset pcm every time to get rid of in-FIFO rx data */
	REG32(PGCR) = PCMENABLE;	// 0->1 enable PCM
	REG32(PGCR) = 0;
	REG32(PGCR) = PCMENABLE|CH0_1;	// 0->1 enable PCM
	REG32(PCSCR) = CH0TE|CH0RE|CH1TE|CH1RE;
	REG32(PTSAR) = 0x00010203;
}

int pcm_probe(void)
{
	uint32 chan;

	/* config GPIO as PCM */
	REG32(0x200c) |= 0x00ff0000UL;
	REG32(0x2054) &= ~0x00ff0000UL;

	REG32(0x203c) = 0xa5000000;
	
	printk("Initialize PCM interface ...\n");
 	REG32(PGCR) = PCMENABLE | PCMCLKDIR;	// 0->1 enable PCM
	REG32(PGCR) = 0;	// 1->0 reset PCM
 	REG32(PGCR) = PCMENABLE | PCMCLKDIR;	// 0->1 enable PCM

	for (chan=0; chan<CH_NUM; chan++)
		pcm_enableChan(chan);

	return 0;
}

void pcm_exit(void)
{
	uint32 chan;

	printk("Exit PCM ...");
	for (chan=0; chan<CH_NUM; chan++)
		pcm_disableChan(chan);
	REG32(PGCR) = 0;	// 1->0 reset PCM
}


